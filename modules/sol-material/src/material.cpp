#include "uuid_system_generator.h"

namespace sol
{
    ////////////////////////////////////////////////////////////////
    // Constructors.
    ////////////////////////////////////////////////////////////////

    Material::Material() { uuid = uuids::uuid_system_generator{}(); }

    Material::~Material() noexcept = default;

    ////////////////////////////////////////////////////////////////
    // Getters.
    ////////////////////////////////////////////////////////////////

    const uuids::uuid& Material::getUuid() const noexcept { return uuid; }

    const std::string& Material::getName() const noexcept { return name; }

    ////////////////////////////////////////////////////////////////
    // Setters.
    ////////////////////////////////////////////////////////////////

    void Material::setName(std::string value) { name = std::move(value); }
}  // namespace sol
