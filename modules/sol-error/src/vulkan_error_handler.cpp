namespace sol
{
    void handleVulkanError(const VkResult result, std::source_location loc)
    {
        if (result == VK_SUCCESS) return;

        throw VulkanError(std::format("A Vulkan error occurred. Error code: {}.", toString(result)), std::move(loc));
    }
}  // namespace sol
